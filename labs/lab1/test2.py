# program to perform all math operations ( + , - , * , / , %)
# program to take input of two numbers and perform the above math operations

# Take num1 and num2 from the user
num1 = int(input("Enter your first number: "))
num2 = int(input("Enter your second number: "))

# Add two numbers
result = num1 + num2
print("Addition: ",result)

# Subtract two numbers
result = num1 - num2
print("Subtraction: ",result)

# divide two numbers
result = num1 / num2
print("Division: ",result)

# mod operation with two numbers
result = num1 % num2
print("Modulo: ",result)

# multiply two numbers
result = num1 * num2
print("Multiply: ",result)